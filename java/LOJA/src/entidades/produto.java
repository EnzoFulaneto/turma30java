package entidades;

public class produto {
	private String codigo;
	private String nome;
	private double valor;
	private int estoque;
	
	
	public produto(String codigo, String nome, double valor, int estoque) {
		super();
		this.codigo = codigo;
		this.nome = nome;
		this.valor = valor;
		this.estoque = estoque;
	}
	
	
}
